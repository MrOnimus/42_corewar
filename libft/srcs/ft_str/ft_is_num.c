#include "libft.h"

int				is_number(char *s)//ft_is_num
{
	if (*s == '-' || ft_isdigit(*s))
	{
		if (ft_is_ok(s + 1, ft_isdigit))
			return (1);
		return (0);
	}
	return (0);
}