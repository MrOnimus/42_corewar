#include "libft.h"

int		ft_word_count(char const *s, char c)
{
	int count;

	if (!s)
		return (0);
	count = 0;
	while (*s)
	{
		while (*s == c && *s)
			s++;
		if (*s)
			count++;
		while (*s != c && *s)
			s++;
	}
	return (count);
}
