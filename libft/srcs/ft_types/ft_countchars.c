#include "libft.h"

int		ft_countchars(int n)
{
	int l;

	l = 0;
	if (n < 0)
	{
		l++;
		n = n * -1;
	}
	if (n == 0)
		l++;
	while (n > 0)
	{
		l++;
		n = n / 10;
	}
	return (l);
}
