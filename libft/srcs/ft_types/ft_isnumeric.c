#include "libft.h"

int		ft_isnumeric(char *str)
{
	size_t	tmp;

	tmp = 0;
	while ((int)str[tmp])
	{
		if (!ft_isdigit(str[tmp]))
			return (0);
		tmp++;
	}
	return (1);
}
